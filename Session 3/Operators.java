
public class Operators {
	public static void main(String[] args) {
		int a = 100;
		int b = 11;
		int c = 50;
		
		int ex1 = a%b;
		int ex2 = a/c;
		int ex3 = a++;
		int ex4 = b--;
		int ex5 = ++b;
		int ex6 = --a;
		
		System.out.println(ex1);
		System.out.println(ex2);
		System.out.println(ex3);
		System.out.println(ex4);
		System.out.println(ex5);
		System.out.println(ex6);
		
	}

}
