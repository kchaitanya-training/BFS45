public class Operators {
    public static void main(String[] args){
        int a = 22;
        int b= 33;
        int c = 2;

        System.out.println(a%c);
        System.out.println(b/c);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--b);
        System.out.println(b--);

    }
}
