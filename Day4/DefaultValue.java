public class DefaultValue {
    String a;
    int b;
    boolean c;
    float d;

    public static void main(String[] args) {
        DefaultValue res = new DefaultValue();
        System.out.println(res.a);
        System.out.println(res.b);
        System.out.println(res.c);
        System.out.println(res.d);
    }
}
