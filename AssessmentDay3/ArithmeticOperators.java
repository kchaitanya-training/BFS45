public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10;

        a++;
        System.out.println(a);

        a--;
        System.out.println(a);

        a = a / 2;
        System.out.println(a);

        a = a * 2;
        System.out.println(a);

        a = a % 2;
        System.out.println(a);
    }
}
