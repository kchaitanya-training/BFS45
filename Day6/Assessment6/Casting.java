package com.day6;

public class Casting {

	public static void main(String[] args) {
		
		// upcasting and downcasting for primitive variables.
		int a = 10;
		double d = (double)a;
		System.out.println(d);
		
		byte b = (byte)a;
		System.out.println(b);
		
		float f = (float)a;
		System.out.println(f);
		
		short s = (short)a;
		System.out.println(s);
		

	}

}
