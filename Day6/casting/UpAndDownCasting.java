package casting;

public class UpAndDownCasting {
    public static void main(String[] args) {

        //Down Casting
        double a = 10;
        int i = (int) a;
        System.out.println(i);

        //Up Casting
        int b = 10;
        double d = (double) b;
        System.out.println(d);

    }
}
